print(1 + 2)      # 1. 1 + 2
print(1 + 4.5)    # 2. 1 + 4.5
print(3 / 2)      # 3. 3 / 2
print(4 / 2)      # 4. 4 / 2
print(3 // 2)     # 5. 3 // 2
print(-3 // 2)    # 6. -3 // 2
print(11 % 2)     # 7. 11 % 2
print(2 ** 10)    # 8. 2 ** 10
print(8 * (1/3))  # 9. 8 * (1/3)
print(int(3.0))    # 2. int(3.0)
print(float("3"))  # 3. float("3")
print(str(12.4))   # 4. str(12.4)
print(bool(0))     # 5. bool(0)
